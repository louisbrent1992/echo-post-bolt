/// Typography constants for consistent font sizing across the app
class AppTypography {
  // Three standardized font sizes for the entire app

  /// Large font size (16px) - Used for primary titles and headers
  /// Examples: "Your image will appear here", "Post Content" header
  static const double large = 16.0;

  /// Body font size (13px) - Used for main content and standard text
  /// Examples: Post content text, button labels, main readable content
  /// Reference: Unified media buttons ("Select Directory", "Select Media")
  static const double body = 13.0;

  /// Small font size (11px) - Used for labels and secondary elements
  /// Examples: "Hashtags" label, transcription status text, metadata
  static const double small = 11.0;

  // Private constructor to prevent instantiation
  AppTypography._();
}
